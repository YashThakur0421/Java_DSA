public class java_17 {

    public static void main(String[] args) {
        int number = 2;
        switch (number) {
            case 1:
                System.out.println("Burger");
                break;
            case 2:
                System.out.println("Pizza");
                break;
            case 3:
                System.out.println("Mango shake");
                break;
            default:
                System.out.println("I wake up");
                break;
        }

    }
}